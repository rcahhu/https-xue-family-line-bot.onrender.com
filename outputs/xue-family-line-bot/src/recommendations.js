export function getRecommendations({ area = "", lat, lng } = {}) {
  const normalizedArea = String(area).trim();
  const catalog =
    catalogs.find((entry) => entry.match.some((keyword) => normalizedArea.includes(keyword))) ||
    fallback;

  return {
    ...catalog,
    areaQuery: normalizedArea || catalog.areaName,
    location: lat && lng ? { lat: Number(lat), lng: Number(lng) } : null,
    source: "built-in-curated-starter"
  };
}

function item(name, tag, note, area) {
  return { name, tag, note, area };
}
